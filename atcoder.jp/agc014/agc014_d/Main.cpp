void solve() {
  int N;
  cin >> N;
  vector<vector<int>> g(N);
  for (int i = 0; i < N - 1; i++) {
    int a, b;
    cin >> a >> b;
    a--;
    b--;
    g[a].push_back(b);
    g[b].push_back(a);
  }
  vector<int> child(N, 0), par(N, -1);
  function<void(int, int)> dfs = [&](int v, int p) {
    par[v] = p;
    for (int u : g[v]) {
      if (u == p) continue;
      child[v]++;
      dfs(u, v);
    }
    return;
  };
  dfs(0, -1);

  dmp(child, par);
  queue<int> q;
  for (int i = 0; i < N; i++)
    if (child[i] == 0) q.push(i);

  vector<bool> matched(N, false);
  while (!q.empty()) {
    int v = q.front();
    q.pop();
    if (par[v] != -1 && !matched[par[v]]) {
      matched[v] = matched[par[v]] = true;
      int pp = par[par[v]];
      if (pp != -1) {
        child[pp]--;
        if (child[pp] == 0) q.push(pp);
      }
    } else {
      cout << "First" << endl;
      return;
    }
  }

  for (int i = 0; i < N; i++) {
    if (!matched[i]) {
      cout << "First" << endl;
      return;
    }
  }
  cout << "Second" << endl;
  return;
}

