template <class T>
vector<T> divisor(T n) {
  vector<T> ret;
  for (T i = 1; i * i <= n; i++) {
    if (n % i == 0) {
      ret.push_back(i);
      if (i * i != n) ret.push_back(n / i);
    }
  }
  sort(begin(ret), end(ret));
  return ret;
}

void solve() {
  int N, C;
  cin >> N >> C;
  auto D = vect(C, vect(C, 0));
  cin >> D;
  auto c = vect(N, vect(N, 0));
  cin >> c;
  auto cnt = vect(3, vect(C, 0));
  for (int i = 0; i < N; i++) {
    for (int j = 0; j < N; j++) { cnt[(i + j) % 3][c[i][j] - 1]++; }
  }
  int ans = INF;
  for (int a = 0; a < C; a++) {
    for (int b = 0; b < C; b++) {
      if (a == b) continue;
      for (int c = 0; c < C; c++) {
        if (a == c || b == c) continue;
        int diff = 0;
        for (int d = 0; d < C; d++) {
          diff += cnt[0][d] * D[d][a];
          diff += cnt[1][d] * D[d][b];
          diff += cnt[2][d] * D[d][c];
        }
        chmin(ans, diff);
      }
    }
  }
  cout << ans << endl;
  return;
}

