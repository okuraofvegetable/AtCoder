#include <atcoder/convolution>

void solve() {
  // cout << generator(200003) << endl;
  const int MOD = 200003;
  const int r = 2;
  vector<int> B(MOD, -1);
  vector<int> pow(2 * MOD, 1);
  B[0] = -INF;
  {
    ll cur = 1;
    int cnt = 0;
    for (int i = 0; i < MOD; i++) {
      B[cur] = cnt;
      pow[cnt] = cur;
      cur = (cur * r) % MOD;
      cnt++;
    }
    for (int i = MOD; i < 2 * MOD; i++) {
      pow[cnt] = cur;
      cur = (cur * r) % MOD;
      cnt++;
    }
    for (int x : B) assert(x != -1);
  }

  int N;
  cin >> N;
  vector<ll> a(MOD, 0);

  ll ans = 0ll;
  for (int i = 0; i < N; i++) {
    ll x;
    cin >> x;
    if (x % MOD == 0) continue;
    ans -= (x * x) % MOD;
    a[B[x]]++;
  }
  auto res = atcoder::convolution_ll(a, a);

  for (int i = 0; i < res.size(); i++) {
    if (res[i] == 0) continue;
    // dmp(i, pow[i], res[i]);
    ans += res[i] * pow[i];
  }
  cout << ans / 2 << endl;
  return;
}

